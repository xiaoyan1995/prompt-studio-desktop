export const NODE_SHORT = 280;
export const NODE_MAX = 560;
export const NODE_MIN = 140;

export function fitNodeToRatio(imgW: number, imgH: number): { w: number; h: number } {
  const ratio = imgW / imgH;
  let w: number, h: number;

  if (ratio >= 1) {
    h = NODE_SHORT;
    w = Math.round(NODE_SHORT * ratio);
  } else {
    w = NODE_SHORT;
    h = Math.round(NODE_SHORT / ratio);
  }

  if (w > NODE_MAX) { h = Math.round(h * (NODE_MAX / w)); w = NODE_MAX; }
  if (h > NODE_MAX) { w = Math.round(w * (NODE_MAX / h)); h = NODE_MAX; }
  if (w < NODE_MIN) w = NODE_MIN;
  if (h < NODE_MIN) h = NODE_MIN;

  return { w, h };
}

